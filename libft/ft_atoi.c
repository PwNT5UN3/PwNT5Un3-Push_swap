int	ft_atoi(const char *n)
{
	int	num;
	int	is_neg;

	is_neg = 0;
	num = 0;
	while ((*n >= 9 && *n <= 13) || *n == 32)
		n++;
	if (*n == '-' || *n == '+')
	{
		if (*n == '-')
			is_neg = 1;
		n++;
	}
	while (*n >= '0' && *n <= '9')
	{
		num *= 10;
		num += (*n - 48);
		n++;
	}
	if (is_neg == 1)
		num *= -1;
	return (num);
}
